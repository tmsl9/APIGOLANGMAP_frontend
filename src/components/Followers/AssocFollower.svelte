<script>
        import axios from "axios";
        import { createEventDispatcher } from "svelte";
        import { userID } from "../../stores/store";
        import { follower } from "../../Routes.svelte";
        import ListFollowers from "./ListFollowers.svelte";
        const dispatch = createEventDispatcher();
        let message = { success: null, display: "" };
        let isSubmitting = false
        let followerID = 0

        $: submit = async () => {
                document.getElementById("submit").disabled = true;
                isSubmitting = true;

                const response = await axios.post(follower.assocFollower, {
                        UserID: $userID,
                        FollowerUserID: parseInt(followerID)
                });

                if (response.status === 201) {
                        message = { success: true, display: response.data.message };
                        isSubmitting = false;
                        dispatch("follower_added", {followers: response.data.followers})
                        document.getElementById("submit").disabled = false;
                } else {
                        message = { success: false, display: response.data.message };
                        isSubmitting = false;
                        document.getElementById("submit").disabled = false;
                }
        }
</script>




	 
	
	<div class="row" style="margin-top:2%">
		
		<div class="col-sm-12">
			
<form on:submit|preventDefault={submit} style="height:20%">
        <!-- svelte-ignore a11y-label-has-associated-control -->
        
        <div class="row">
                
                <div class="col-sm-6" style="margin-left:30px;">
                        <label style="color:black;">Follower ID</label>
                <input name="followerID" style="width:75%; display:flex; justify-content:center;align-items:center;"  placeholder="Set Follower ID" bind:value={followerID} />
                </div>
                
                        <button type="submit" id="submit" style="margin-top:4%;width:100px;display:inline-block;">
                                {#if isSubmitting}Adding...{:else}Add{/if}
                        </button>
                
        </div>

       
</form>
<div class="row" style="margin:3%; margin-top: 8%">
{#if message.success != null}
        <div class="alert {message.success ? 'alert-success' : 'alert-danger'}" role="alert">
                {message.display}
        </div>
{/if}
		</div>
	</div>
                
	</div>
	


